"""
Dataset loader for Unity simulator data (colon reconstruction simulator).

Each sequence folder must contain:
  - *.png images (already 224×224)
  - gt_cameras.npz  with keys: gtR (N,3,3), gtT (N,3), gtFL (N,2)
    all in PyTorch3D convention (R_wc, t_cw, NDC focal length)

Generated by simulator_to_gt_cameras.py
"""

import os
import os.path as osp
import random

import numpy as np
import torch
from PIL import Image, ImageFile
from pytorch3d.renderer import PerspectiveCameras
from torch.utils.data import Dataset
from torchvision import transforms

from util.normalize_cameras import normalize_cameras
from util.camera_transform import adjust_camera_to_bbox_crop_, adjust_camera_to_image_scale_, bbox_xyxy_to_xywh

Image.MAX_IMAGE_PIXELS = None
ImageFile.LOAD_TRUNCATED_IMAGES = True


class SimulatorDataset(Dataset):
    def __init__(
        self,
        data_dir,                       # root dir containing sequence subfolders
        split="train",                  # "train" or "test"
        train_split_ratio=0.9,          # fraction of sequences used for train
        img_size=224,
        eval_time=False,
        normalize_cameras=True,
        first_camera_transform=True,
        min_num_images=10,
        color_aug=True,
        erase_aug=False,
        jitter_scale=[0.8, 1.2],
        jitter_trans=[-0.07, 0.07],
        sort_by_filename=False,
        compute_optical=False,
        seed=0,
    ):
        self.data_dir = data_dir
        self.img_size = img_size
        self.eval_time = eval_time
        self.normalize_cameras = normalize_cameras
        self.first_camera_transform = first_camera_transform
        self.sort_by_filename = sort_by_filename
        self.compute_optical = compute_optical
        self.color_aug = color_aug
        self.erase_aug = erase_aug

        if eval_time:
            self.jitter_scale = [1, 1]
            self.jitter_trans = [0, 0]
        else:
            self.jitter_scale = jitter_scale
            self.jitter_trans = jitter_trans

        self.transform = transforms.Compose([
            transforms.ToTensor(),
            transforms.Resize(img_size, antialias=True),
        ])

        if self.color_aug:
            self.color_jitter = transforms.Compose([
                transforms.RandomApply(
                    [transforms.ColorJitter(brightness=0.4, contrast=0.4, saturation=0.2, hue=0.1)], p=0.65
                ),
                transforms.RandomGrayscale(p=0.15),
            ])
        if self.erase_aug:
            self.rand_erase = transforms.RandomErasing(
                p=0.1, scale=(0.02, 0.33), ratio=(0.3, 3.3), value=0, inplace=False
            )

        # ── scan sequence folders ──────────────────────────────────────────
        # Each subfolder with gt_cameras.npz is one sequence
        all_seqs = sorted([
            d for d in os.listdir(data_dir)
            if osp.isdir(osp.join(data_dir, d))
            and osp.exists(osp.join(data_dir, d, "gt_cameras.npz"))
        ])

        if len(all_seqs) == 0:
            raise FileNotFoundError(f"No valid sequence folders (with gt_cameras.npz) found in {data_dir}")

        # deterministic train/test split by sequence
        rng = random.Random(seed)
        shuffled = all_seqs[:]
        rng.shuffle(shuffled)
        n_train = max(1, int(len(shuffled) * train_split_ratio))
        if split == "train":
            selected_seqs = shuffled[:n_train]
        else:
            selected_seqs = shuffled[n_train:] if n_train < len(shuffled) else shuffled  # fallback if only 1 seq

        # ── load metadata for each sequence ───────────────────────────────
        self.sequences = {}   # seq_name -> list of {image_path, R, T, fl, pp}

        for seq_name in selected_seqs:
            seq_dir = osp.join(data_dir, seq_name)
            npz = np.load(osp.join(seq_dir, "gt_cameras.npz"))
            gtR  = npz["gtR"].astype(np.float32)   # (N, 3, 3)
            gtT  = npz["gtT"].astype(np.float32)   # (N, 3)
            gtFL = npz["gtFL"].astype(np.float32)  # (N, 2)
            N = len(gtR)

            # principal point is [0, 0] (image center in NDC) — matches simulator intrinsics
            gtPP = np.zeros((N, 2), dtype=np.float32)

            # match sorted PNGs to frame order
            pngs = sorted([f for f in os.listdir(seq_dir) if f.lower().endswith(".png")])
            if len(pngs) < min_num_images:
                continue
            if len(pngs) != N:
                # if counts differ, use min overlap
                use_n = min(len(pngs), N)
                pngs = pngs[:use_n]
                gtR, gtT, gtFL, gtPP = gtR[:use_n], gtT[:use_n], gtFL[:use_n], gtPP[:use_n]

            frames = []
            for i, png in enumerate(pngs):
                frames.append({
                    "image_path": osp.join(seq_dir, png),
                    "R":  gtR[i],
                    "T":  gtT[i],
                    "fl": gtFL[i],
                    "pp": gtPP[i],
                })
            self.sequences[seq_name] = frames

        self.sequence_list = list(self.sequences.keys())
        print(f"SimulatorDataset [{split}]: {len(self.sequence_list)} sequences from {data_dir}")

    # ── Dataset interface ──────────────────────────────────────────────────

    def __len__(self):
        return len(self.sequence_list)

    def __getitem__(self, idx_N):
        """Called by DynamicBatchSampler with (index, n_per_seq) tuple."""
        index, n_per_seq = idx_N
        seq_name = self.sequence_list[index]
        frames = self.sequences[seq_name]
        n_per_seq = min(n_per_seq, len(frames))
        ids = np.random.choice(len(frames), n_per_seq, replace=False)
        return self.get_data(index=index, ids=ids)

    def get_data(self, index=None, sequence_name=None, ids=(0, 1), no_images=False):
        if sequence_name is None:
            sequence_name = self.sequence_list[index]
        frames = self.sequences[sequence_name]

        annos = [frames[i] for i in ids]
        if self.sort_by_filename:
            annos = sorted(annos, key=lambda x: x["image_path"])

        images, rotations, translations, focal_lengths, principal_points = [], [], [], [], []

        for anno in annos:
            image = Image.open(anno["image_path"]).convert("RGB")
            images.append(image)
            rotations.append(torch.tensor(anno["R"]))
            translations.append(torch.tensor(anno["T"]))
            focal_lengths.append(torch.tensor(anno["fl"]))
            principal_points.append(torch.tensor(anno["pp"]))

        # ── crop & resize ──────────────────────────────────────────────────
        # Images are already square (224×224), so bbox = full image.
        # We still run adjust_camera_to_bbox_crop_ / adjust_camera_to_image_scale_
        # so the pipeline is identical to Co3dDataset.
        images_transformed = []
        new_fls, new_pps, crop_parameters = [], [], []

        for i, (anno, image) in enumerate(zip(annos, images)):
            w, h = image.width, image.height
            # center square crop (identity for square images)
            min_dim = min(h, w)
            top  = (h - min_dim) // 2
            left = (w - min_dim) // 2
            bbox = np.array([left, top, left + min_dim, top + min_dim])

            if not self.eval_time:
                bbox = self._jitter_bbox(bbox)

            bbox_xywh = torch.FloatTensor(bbox_xyxy_to_xywh(bbox))
            fl_crop, pp_crop = adjust_camera_to_bbox_crop_(
                focal_lengths[i], principal_points[i],
                torch.FloatTensor([w, h]), bbox_xywh
            )

            image_crop = transforms.functional.crop(
                image, top=int(bbox[1]), left=int(bbox[0]),
                height=int(bbox[3] - bbox[1]), width=int(bbox[2] - bbox[0])
            )

            new_fl, new_pp = adjust_camera_to_image_scale_(
                fl_crop, pp_crop,
                torch.FloatTensor([image_crop.width, image_crop.height]),
                torch.FloatTensor([self.img_size, self.img_size]),
            )

            new_fls.append(new_fl)
            new_pps.append(new_pp)
            images_transformed.append(self.transform(image_crop))

            crop_center = (bbox[:2] + bbox[2:]) / 2
            cc = (2 * crop_center / min(h, w)) - 1
            crop_width = 2 * (bbox[2] - bbox[0]) / min(h, w)
            crop_parameters.append(torch.tensor([-cc[0], -cc[1], crop_width]).float())

        new_fls = torch.stack(new_fls)
        new_pps = torch.stack(new_pps)

        batch = {
            "seq_id":   sequence_name,
            "category": "simulator",
            "n":        len(frames),
            "ind":      torch.tensor(ids),
        }

        # ── optional camera normalisation ──────────────────────────────────
        if self.normalize_cameras:
            cameras = PerspectiveCameras(
                focal_length=new_fls.numpy(),
                principal_point=new_pps.numpy(),
                R=np.stack([anno["R"] for anno in annos]),
                T=np.stack([anno["T"] for anno in annos]),
            )
            norm_cameras = normalize_cameras(
                cameras,
                compute_optical=self.compute_optical,
                first_camera=self.first_camera_transform,
            )
            if norm_cameras == -1:
                raise RuntimeError("Camera normalisation failed (scale = 0)")

            batch["R"]          = norm_cameras.R
            batch["T"]          = norm_cameras.T
            batch["fl"]         = norm_cameras.focal_length
            batch["pp"]         = norm_cameras.principal_point
            batch["R_original"] = torch.stack([torch.tensor(a["R"]) for a in annos])
            batch["T_original"] = torch.stack([torch.tensor(a["T"]) for a in annos])
        else:
            batch["R"]  = torch.stack(rotations)
            batch["T"]  = torch.stack(translations)
            batch["fl"] = new_fls
            batch["pp"] = new_pps

        batch["crop_params"] = torch.stack(crop_parameters)

        # ── colour augmentation ────────────────────────────────────────────
        images_out = torch.stack(images_transformed)
        if self.color_aug and not self.eval_time:
            images_out = self.color_jitter(images_out)
            if self.erase_aug:
                images_out = self.rand_erase(images_out)

        batch["image"] = images_out
        return batch

    # ── helpers ────────────────────────────────────────────────────────────

    def _jitter_bbox(self, bbox):
        from datasets.co3d_v2 import square_bbox
        bbox = square_bbox(bbox.astype(np.float32))
        s  = np.random.uniform(self.jitter_scale[0], self.jitter_scale[1])
        tx, ty = np.random.uniform(self.jitter_trans[0], self.jitter_trans[1], size=2)
        side = bbox[2] - bbox[0]
        center = (bbox[:2] + bbox[2:]) / 2 + np.array([tx, ty]) * side
        extent = side / 2 * s
        ul = (center - extent).round().astype(int)
        lr = ul + np.round(2 * extent).astype(int)
        return np.concatenate((ul, lr))
